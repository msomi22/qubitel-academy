import { readdirSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const academiesRoot = resolve(projectRoot, 'src/academies');
const outputPath = resolve(academiesRoot, 'manifestImports.generated.js');

function walk(directory) {
  return readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const path = resolve(directory, entry.name);
    return entry.isDirectory() ? walk(path) : [path];
  });
}

function importName(path) {
  return path
    .replace(/^\.\//, '')
    .replace(/\/(academy|category|topic)\.manifest\.json$/, '_$1')
    .replace(/[^a-z0-9]+/gi, '_')
    .replace(/^_|_$/g, '');
}

function recordType(path) {
  if (path.endsWith('/academy.manifest.json')) return 'academy';
  if (path.endsWith('/category.manifest.json')) return 'category';
  return 'topic';
}

const records = walk(academiesRoot)
  .filter((path) => /\/(academy|category|topic)\.manifest\.json$/.test(path))
  .map((path) => {
    const relativePath = `./${relative(academiesRoot, path).replaceAll('\\', '/')}`;
    return {
      path: relativePath,
      name: importName(relativePath),
      type: recordType(relativePath)
    };
  })
  .sort((a, b) => a.path.localeCompare(b.path));

const lines = [
  '// Generated by scripts/generate-academy-manifest-imports.mjs. Do not edit manually.',
  ...records.map((record) => (
    `import ${record.name} from '${record.path}' with { type: 'json' };`
  )),
  ''
];

for (const type of ['academy', 'category', 'topic']) {
  lines.push(`export const ${type}ManifestRecords = [`);
  lines.push(...records
    .filter((record) => record.type === type)
    .map((record) => `  { path: '${record.path}', manifest: ${record.name} },`));
  lines[lines.length - 1] = lines.at(-1).replace(/,$/, '');
  lines.push('];', '');
}

writeFileSync(outputPath, `${lines.join('\n').trim()}\n`);
console.log(`Generated ${relative(projectRoot, outputPath)} with ${records.length} manifests.`);
